package com.baidubce.services.iotdm.model.v3.rules;

import com.baidubce.services.iotdm.model.v3.schema.SchemaProperty.PropertyType;

public class DeviceRuleSource {
    /**
     * Description of Device Rule .
     */
    private String description;
    /**
     * Name of Device Rule .
     */
    private String name;
    /**
     * Type of Device Rule .
     */
    private PropertyType type;
    /**
     * Condition of Device Rule .
     */
    private String condition;
    /**
     * Value of Device Rule .
     */
    private String value;

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public PropertyType getType() {
        return type;
    }

    public void setType(PropertyType type) {
        this.type = type;
    }

    public String getCondition() {
        return condition;
    }

    public void setCondition(String condition) {
        this.condition = condition;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
